const servicesGrid = document.querySelector("#servicesGrid");
const langToggle = document.querySelector("#langToggle");
const html = document.documentElement;
let currentLang = localStorage.getItem("scienceInMotionLang") || "el";

function renderServices(lang) {
  servicesGrid.innerHTML = services.map((service) => `
    <article class="service-card">
      <div class="service-image">
        <img src="${service.image}" alt="${service[lang].title}" loading="lazy">
      </div>
      <div class="service-body">
        <h3>${service[lang].title}</h3>
        <p>${service[lang].text}</p>
      </div>
    </article>
  `).join("");
}

function applyLanguage(lang) {
  currentLang = lang;
  html.lang = lang;
  html.dataset.lang = lang;
  localStorage.setItem("scienceInMotionLang", lang);

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    element.textContent = translations[lang][key];
  });

  langToggle.innerHTML = lang === "el"
    ? '<span class="lang-active">EL</span><span>EN</span>'
    : '<span>EL</span><span class="lang-active">EN</span>';

  renderServices(lang);
}

langToggle.addEventListener("click", () => {
  applyLanguage(currentLang === "el" ? "en" : "el");
});

document.querySelector("#year").textContent = new Date().getFullYear();
applyLanguage(currentLang);
